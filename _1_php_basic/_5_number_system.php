<?php

    //declare decimal number. decimal numbers are between 0-9
    $n = 12;

    //declare octal number.Octal number start with zero (0). octal numbers are between 0-7
    $oct = 012;

    //declare hexadecimal number.Hexadecimal number start with 0x. Hexa numbers are between 0-9 and A-F
    $hex = 0x12;

    //print decimal number in decimal
    printf("After convert decimal to decimal, the number is %d. \n",$n);

    //print octal number in decimal
    printf("After convert octal to decimal, the number is %d. \n",$oct);

    //print hexa number in decimal
    printf("After convert hexa to decimal, the number is %d. \n",$hex);

    //print decimal number in binary
    printf("After convert decimal to binary, the number is %b. \n",$n);